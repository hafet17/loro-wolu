    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */

    public function store(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'name' => 'required|max:255',
            'email' => 'required|unique:users|max:255',
            'password' => 'required|max:255',
            'amount' => 'required|integer',
        ]);

        if ($validator->fails()) {
            return redirect()->route('user.index')
                ->withErrors($validator)
                ->withInput();
        }

        DB::beginTransaction();

        try {
            // Step 1 : Create User
            $user = new User();
            $user->name = $request->name;
            $user->email = $request->email;
            $user->password = bcrypt($request->password);
            $user->save();

            //Step 2 : Amount Charged
            $user_amount = new UserAmount();
            $user_amount->user_id = $user->id;
            $user_amount->amount = $request->amount;
            $user_amount->save();

            DB::commit();

            return redirect()->route('user.index')->with('status', 'Thank You for your subscription');
        } catch (\Exception $e) {
                DB::rollback();
                return redirect()->route('user.index')
                    ->with('status', 'Sorry Database rollback!');
        }
    }

